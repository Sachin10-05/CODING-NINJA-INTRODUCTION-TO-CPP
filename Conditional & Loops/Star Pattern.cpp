/*
Problem:

Print the following pattern.
For N=4
   *
  ***
 *****
*******
*/

//********************************************************************************************************************************************************************

//using FOR loop
//**************

#include<bits/stdc++.h>
using namespace std;
int main()
{
   int N;
   cin >> N;
   for(int i=1;i<=N;i++)
   {
      for(int j=N-i;j>=1;j--)
      {
         cout << " ";
      }
	   
      for(int j=1;j<2*i;j++)
      {
         cout << "*";
      }
      cout << "\n";
   }
}



